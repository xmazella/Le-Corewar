#include "libft.h"

/*
** DESCRIPTION
**	- The ft_lstiter() function applies the f function on every links of the
**	linked list pointed by lst.
**
** NOTE
**	- See libft_typedefs.h to see the t_list structure.
*/

void			ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst);
		lst = lst->next;
	}
}
