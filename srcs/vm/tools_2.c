#include "vm.h"

int								verbose_level(t_vm *vm, int level)
{
	return ((vm->debug & level) && !vm->visu);
}

size_t							byte_reverse(size_t n)
{
	return ((n & 0xff) << 16
			| (n & 0xff00) << 8
			| (n & 0xff0000) >> 8
			| (n & 0xff000000) >> 24);
}
